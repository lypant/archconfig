# Set console keymap
cp "$ISO_ARCHCONFIG_PATH/$SET_CONSOLE_KEYMAP_FILE" "$DST_DIR/$SET_CONSOLE_KEYMAP_FILE"

# Set console font
cp "$ISO_ARCHCONFIG_PATH/$SET_CONSOLE_FONT_FILE" "$DST_DIR/$SET_CONSOLE_FONT_FILE"

# Network

# Configure network
cp "$ISO_ARCHCONFIG_PATH/$CONFIGURE_NETWORK_FILE" "$DST_DIR/$CONFIGURE_NETWORK_FILE"

# Bootloader

# Install bootloader
cp "$ISO_ARCHCONFIG_PATH/$INSTALL_BOOTLOADER_FILE" "$DST_DIR/$INSTALL_BOOTLOADER_FILE"

# Configure bootloader
cp "$ISO_ARCHCONFIG_PATH/$CONFIGURE_BOOTLOADER_FILE" "$DST_DIR/$CONFIGURE_BOOTLOADER_FILE"

# Users

# Set root password
cp "$ISO_ARCHCONFIG_PATH/$SET_ROOT_PASSWORD_FILE" "$DST_DIR/$SET_ROOT_PASSWORD_FILE"

# Add regular user
cp "$ISO_ARCHCONFIG_PATH/$ADD_REGULAR_USER_FILE" "$DST_DIR/$ADD_REGULAR_USER_FILE"

# Set regular user password
cp "$ISO_ARCHCONFIG_PATH/$SET_REGULAR_USER_PASSWORD_FILE" "$DST_DIR/$SET_REGULAR_USER_PASSWORD_FILE"

# Set sudoers
cp "$ISO_ARCHCONFIG_PATH/$SET_SUDOERS_FILE" "$DST_DIR/$SET_SUDOERS_FILE"

# Customization

# Update package list
