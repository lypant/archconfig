cp "$ISO_ARCHCONFIG_PATH/$PARTITION_DISKS_FILE" "$DST_DIR/$PARTITION_DISKS_FILE"
cp "$ISO_ARCHCONFIG_PATH/$CREATE_FILE_SYSTEMS_FILE" "$DST_DIR/$CREATE_FILE_SYSTEMS_FILE"
