# System
cp "$ISO_ARCHCONFIG_PATH/$INSTALL_BASE_SYSTEM_FILE" "$DST_DIR/$INSTALL_BASE_SYSTEM_FILE"

# Fstab
cp "$ISO_ARCHCONFIG_PATH/$GENERATE_FSTAB_FILE" "$DST_DIR/$GENERATE_FSTAB_FILE"

# Chroot

# Chroot
cp "$ISO_ARCHCONFIG_PATH/$CHROOT_FILE" "$DST_DIR/$CHROOT_FILE"

# Host name
cp "$ISO_ARCHCONFIG_PATH/$SET_HOST_NAME_FILE" "$DST_DIR/$SET_HOST_NAME_FILE"

# Localization

# Set locales
cp "$ISO_ARCHCONFIG_PATH/$SET_LOCALES_FILE" "$DST_DIR/$SET_LOCALES_FILE"

# Generate locales
cp "$ISO_ARCHCONFIG_PATH/$GENERATE_LOCALES_FILE" "$DST_DIR/$GENERATE_LOCALES_FILE"

# Set language
cp "$ISO_ARCHCONFIG_PATH/$SET_LANGUAGE_FILE" "$DST_DIR/$SET_LANGUAGE_FILE"

# Set time zone
cp "$ISO_ARCHCONFIG_PATH/$SET_TIME_ZONE_FILE" "$DST_DIR/$SET_TIME_ZONE_FILE"

