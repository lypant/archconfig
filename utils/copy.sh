cp "$ISO_ARCHCONFIG_PATH/$GITCONFIG_FILE" "$DST_DIR/$GITCONFIG_FILE"
cp "$ISO_ARCHCONFIG_PATH/$VIMRC_FILE" "$DST_DIR/$VIMRC_FILE"
